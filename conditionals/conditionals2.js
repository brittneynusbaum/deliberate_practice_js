// 2. Use a variable to store a number, then write a condition that prints -1 if the number is less than 10, prints 1 if the number is greater than 10, and prints 0 if the number is equal to 10.

var x = 1;

if (x < 10) {
  console.log(-1);
} else if (x > 10) {
  console.log(1);
} else if (x === 10) {
  console.log(0);
}